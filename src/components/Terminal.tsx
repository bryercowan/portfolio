export function Terminal() {
  const [history, setHistory] = useState<Array<{ command: string, output: string | string[] }>>([]);
  const [currentCommand, setCurrentCommand] = useState('');
  const [viewingMarkdown, setViewingMarkdown] = useState<string | null>(null);
  const [playingTetris, setPlayingTetris] = useState(false);
  const terminalRef = useRef<HTMLDivElement>(null);
  const inputRef = useRef<HTMLInputElement>(null);

  const handleKeyPress = useCallback((e: KeyboardEvent) => {
    if (viewingMarkdown && e.key === 'q' && e.target === document.body) {
      e.preventDefault();
      setViewingMarkdown(null);
    }
  }, [viewingMarkdown]);

  useEffect(() => {
    document.addEventListener('keypress', handleKeyPress);
    return () => document.removeEventListener('keypress', handleKeyPress);
  }, [handleKeyPress]);

  useEffect(() => {
    setHistory([{
      command: 'welcome',
      output: [
        ASCII_ART,
        'Welcome to Bryer Cowan\'s Terminal Portfolio!',
        'Type "help" to see available commands.'
      ]
    }]);
  }, []);

  useEffect(() => {
    if (terminalRef.current) {
      terminalRef.current.scrollTop = terminalRef.current.scrollHeight;
    }
  }, [history, currentCommand]);

  const handleGameOver = async (score: number) => {
    const playerName = prompt('Game Over! Enter your name for the leaderboard:');
    if (playerName) {
      await supabase
        .from('tetris_scores')
        .insert([{ player_name: playerName, score }]);
    }
    setPlayingTetris(false);
    setHistory(prev => [...prev, {
      command: 'game',
      output: `Game Over! Final Score: ${score}`
    }]);
  };

  const handleCommand = async (cmd: string) => {
    const normalizedCmd = cmd.toLowerCase().trim();

    if (normalizedCmd === ':q' && viewingMarkdown) {
      setViewingMarkdown(null);
      return;
    }

    if (viewingMarkdown) {
      return;
    }

    const checkProfanity = async (text: string): Promise<boolean> => {
      try {
        const response = await fetch(`https://www.purgomalum.com/service/containsprofanity?text=${encodeURIComponent(text)}`);
        const result = await response.text();
        return result === 'true';
      } catch (error) {
        console.error('Error checking profanity:', error);
        return false; // In case of API failure, let's err on the side of allowing the comment
      }
    };

    let output: string | string[] = '';

    if (normalizedCmd.startsWith('comment -m ')) {
      const message = cmd.slice(10).trim();
      if (!message.startsWith('"') || !message.endsWith('"')) {
        output = 'Error: Comment must be wrapped in quotes. Example: comment -m "Your message here"';
      } else {
        const commentText = message.slice(1, -1).trim();
        if (commentText.length === 0) {
          output = 'Error: Comment cannot be empty';
        } else {
          const hasProfanity = await checkProfanity(commentText);
          if (hasProfanity) {
            output = 'Error: Comment contains inappropriate language';
          } else {
            try {
              await supabase
                .from('comments')
                .insert([{ message: commentText }]);
              output = 'Comment added successfully!';
            } catch (error) {
              output = 'Error: Failed to add comment';
            }
          }
        }
      }
    } else if (normalizedCmd === 'comment') {
      try {
        const { data: comments, error } = await supabase
          .from('comments')
          .select('*')
          .order('created_at', { ascending: false });

        if (error) throw error;

        if (!comments || comments.length === 0) {
          output = 'No comments yet. Be the first to add one!';
        } else {
          output = ['Comments:', '', ...comments.map(c =>
            `[${new Date(c.created_at).toLocaleString()}] ${c.message}`
          )];
        }
      } catch (error) {
        output = 'Error: Failed to fetch comments';
      }
    } else {
      switch (normalizedCmd) {
        case 'help':
          output = COMMANDS.help;
          break;
        case 'about':
        case 'experience':
        case 'skills':
        case 'projects':
        case 'contact':
          setViewingMarkdown(COMMANDS[normalizedCmd]);
          output = `Opening ${normalizedCmd} in markdown viewer... (press :q to exit)`;
          break;
        case 'art':
          output = ASCII_COLLECTION[Math.floor(Math.random() * ASCII_COLLECTION.length)];
          break;
        case 'comment':
          break;
        //case 'game':
        //  setPlayingTetris(true);
        //  output = [
        //    'Welcome to Terminal Tetris!',
        //    'Controls:',
        //    '  ↑ - Rotate',
        //    '  ← → - Move left/right',
        //    '  ↓ - Drop',
        //    '  Space - Pause',
        //    '',
        //    'Game starting...'
        //  ];
        //  break;
        //case 'leaderboard':
        //  const { data: scores } = await supabase
        //    .from('tetris_scores')
        //    .select('*')
        //    .order('score', { ascending: false })
        //    .limit(3);
        //
        //  output = [
        //    '🏆 Tetris Leaderboard 🏆',
        //    '',
        //    ...(scores || []).map((score, index) =>
        //      `${index + 1}. ${score.player_name} - ${score.score} points`
        //    )
        //  ];
        //  break;
        case 'clear':
          setHistory([]);
          return;
        default:
          if (normalizedCmd === '') {
            output = '';
          } else {
            output = `Command not found: ${cmd}. Type "help" for available commands.`;
          }
      }
    }

    setHistory(prev => [...prev, { command: cmd, output }]);
    setCurrentCommand('');
  };

  const handleInputKeyPress = (e: React.KeyboardEvent) => {
    if (e.key === 'Enter') {
      handleCommand(currentCommand);
      setCurrentCommand('');
    }
  };

  const focusInput = () => {
    if (!viewingMarkdown && !playingTetris) {
      inputRef.current?.focus();
    }
  };

  return (
    <div className="terminal" onClick={focusInput} ref={terminalRef}>
      {!viewingMarkdown && !playingTetris ? (
        <>
          <div className="terminal-content">
            {history.map((entry, i) => (
              <div key={i} className="command-block">
                <div className="command-line">
                  <span className="prompt">visitor@portfolio:~$</span>
                  <span className="command ml-2">{entry.command}</span>
                </div>
                {entry.output && (
                  <div className="output">
                    {Array.isArray(entry.output) ? (
                      entry.output.map((line, j) => (
                        <div key={j} className="output-line">{line}</div>
                      ))
                    ) : (
                      <div className="output-line">{entry.output}</div>
                    )}
                  </div>
                )}
              </div>
            ))}
            <div className="command-line current">
              <span className="prompt">visitor@portfolio:~$</span>
              <input
                ref={inputRef}
                type="text"
                value={currentCommand}
                onChange={(e) => setCurrentCommand(e.target.value)}
                onKeyPress={handleInputKeyPress}
                className="command-input"
                autoFocus
              />
            </div>
          </div>
        </>
      ) : playingTetris ? (
        <TetrisGame onGameOver={handleGameOver} />
      ) : (
        <div className="markdown-viewer">
          <div className="markdown-header">
            <span className="text-yellow-500">-- MARKDOWN VIEWER --</span>
            <span className="text-gray-500">Type :q to exit</span>
          </div>
          <div className="markdown-content">
            <ReactMarkdown>{viewingMarkdown}</ReactMarkdown>
          </div>
        </div>
      )}
    </div>
  );
}
